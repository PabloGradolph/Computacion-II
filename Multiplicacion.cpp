//Multiplicación de números introducidos por terminal
#include <iostream>
using namespace std;

int main(){

    float num1, num2;
    cout<<"Escribe un real: "; cin>>num1;

    cout<<"Escribe otro real: "; cin>>num2;

    cout<<num1<< " * " << num2<< " = " << num1*num2<<endl;
    
    return 0;
    
}